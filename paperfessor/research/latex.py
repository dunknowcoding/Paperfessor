"""LaTeX build pipeline.

Converts a paper's markdown to a venue-style .tex file, then compiles
it with :mod:`pdflatex` to ``paper.pdf``. We default to the
``acmart`` class (ships with TeX Live) in ``sigconf`` mode, which is
the format used by NeurIPS, ICML, and most CS conferences.

The .tex file is generated by walking the markdown structure
(headings, lists, paragraphs, citations) and emitting the matching
LaTeX. It is intentionally minimal: it does NOT try to support every
markdown feature. Unsupported features degrade to a paragraph with
the markdown text verbatim.

The LaTeX file is written to ``workspace/paper/body/paper.tex`` and
the PDF to ``workspace/paper/body/paper.pdf``.
"""

